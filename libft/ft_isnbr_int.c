#include "libft.h"

int		ft_isnbr_int(char *str)
{
	int		i;
	int		i2;

	i = 0;
	i2 = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] == '0')
		i++;
	while (str[i] != '\0')
	{
		if (ft_isdigit(str[i]) == 0 || (i2 == 10 && str[i] > 7))
			return (0);
		i2++;
		i++;
	}
	return (1);
}
